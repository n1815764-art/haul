import { Order, TrackingEvent } from '../types/order';

const createTrackingEvents = (status: Order['status']): TrackingEvent[] => {
  const now = new Date();
  const events: TrackingEvent[] = [
    {
      id: 'event-1',
      status: 'Order Placed',
      description: 'Your order has been received',
      location: 'Online',
      timestamp: new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000).toISOString(),
      completed: true,
    },
    {
      id: 'event-2',
      status: 'Processing',
      description: 'Your order is being prepared',
      location: 'Warehouse',
      timestamp: new Date(now.getTime() - 6 * 24 * 60 * 60 * 1000).toISOString(),
      completed: ['processing', 'shipped', 'delivered'].includes(status),
    },
    {
      id: 'event-3',
      status: 'Shipped',
      description: 'Your order has left our facility',
      location: 'Distribution Center',
      timestamp: new Date(now.getTime() - 4 * 24 * 60 * 60 * 1000).toISOString(),
      completed: ['shipped', 'delivered'].includes(status),
    },
    {
      id: 'event-4',
      status: 'In Transit',
      description: 'Your order is on its way',
      location: 'Regional Hub',
      timestamp: new Date(now.getTime() - 2 * 24 * 60 * 60 * 1000).toISOString(),
      completed: status === 'delivered',
    },
    {
      id: 'event-5',
      status: 'Out for Delivery',
      description: 'Your order will arrive today',
      location: 'Local Facility',
      timestamp: new Date(now.getTime() - 12 * 60 * 60 * 1000).toISOString(),
      completed: status === 'delivered',
    },
    {
      id: 'event-6',
      status: 'Delivered',
      description: 'Your order has been delivered',
      location: 'Front Door',
      timestamp: new Date(now.getTime() - 2 * 60 * 60 * 1000).toISOString(),
      completed: status === 'delivered',
    },
  ];

  return events;
};

