export const getProductById = (id: string): Product | undefined => {
  return mockProducts.find(p => p.id === id);
};

export const getProductsByVibe = (vibes: string[]): Product[] => {
  return mockProducts.filter(p => 
    vibes.some(v => p.vibes.includes(v))
  );
};

export const getRandomMatchup = (): { productA: Product; productB: Product } => {
  const shuffled = [...mockProducts].sort(() => Math.random() - 0.5);
  return {
    productA: shuffled[0],
    productB: shuffled[1],
  };
};

export const getProductsByCategory = (category: string): Product[] => {
  return mockProducts.filter(p => p.category === category);
};

export const getTrendingProducts = (): Product[] => {
  return mockProducts.filter(p => p.isTrending);
};

export const getNewArrivals = (): Product[] => {
  return mockProducts.filter(p => p.isNew);
};
